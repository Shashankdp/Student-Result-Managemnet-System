    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed
           // TODO add your handling code here:
           //now i will create database.
           // here i will store all the data in the variables,these help us to store the data in the database.
           String course=(String)jComboBox1.getSelectedItem();
           String branch=(String)jComboBox2.getSelectedItem();
           String rollno=jTextField1.getText();
           String name=jTextField2.getText();
           String gender=(String)jComboBox3.getSelectedItem();
           String fathersname=jTextField3.getText();
           
           // now i trying to push the data in database.
           try{
               //now  i want to connect with my db.
               Class.forName("com.mysql.jdbc.Driver");
               Connection con=DriverManager.getConnection("jdbc:mysql://localhost:3306/shashank","root","Shashank@2");
               //above line, driver manager try to give connection with that url. that url contains the username and password.
               
               Statement st=con.createStatement();
               st.executeUpdate("insert into student values('"+rollno+"','"+course+"','"+branch+"','"+name+"','"+gender+"','"+fathersname+"')");
               JOptionPane.showMessageDialog(null,"Data updated successfully");      //this is a dialogbox line.
               setVisible(false);
               new adminIndex().setVisible(true);
               
           }
           catch(Exception e){
               JOptionPane.showMessageDialog(null,e.toString());
           }
           
    }//GEN-LAST:event_jButton6ActionPerformed

